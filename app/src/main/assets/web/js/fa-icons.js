const FA_BY_NAME = Object.fromEntries(FA_ICONS.map(([name, hex, aliases]) => [name, { name, hex, aliases }]));

function faGlyph(name) {
  const icon = FA_BY_NAME[name];
  if (!icon) return "?";
  return String.fromCodePoint(parseInt(icon.hex, 16));
}

function faSearch(query) {
  const q = (query || "").trim().toLowerCase();
  if (!q) return FA_ICONS.map(([name]) => name);
  return FA_ICONS.filter(([name, , aliases]) =>
    name.includes(q) || aliases.some((a) => a.includes(q))
  ).map(([name]) => name);
}

/** Resolves every static [data-icon] element in the DOM to its Font Awesome glyph. */
function applyStaticIcons(root) {
  (root || document).querySelectorAll("[data-icon]").forEach((el) => {
    el.textContent = faGlyph(el.getAttribute("data-icon"));
  });
}
